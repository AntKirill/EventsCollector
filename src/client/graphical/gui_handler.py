    def delete_calendar(self, containing_layout):
        vlayout = self.verticalLayout
        for i in range(vlayout.count()):
            layout_item = vlayout.itemAt(i)
            if layout_item.layout() == containing_layout:
                for j in reversed(range(containing_layout.count())):
                    containing_layout.itemAt(j).widget().deleteLater()
                vlayout.removeItem(layout_item)
                return

    def add_calendar(self, containing_layout, calendar_name=''):
        new_horizontal_layout = QtWidgets.QHBoxLayout()
        new_calendar_name = QtWidgets.QLineEdit(self.scrollAreaWidgetContents)
        new_calendar_name.setText(calendar_name)
        new_calendar_name.setAlignment(QtCore.Qt.AlignCenter)
        new_horizontal_layout.addWidget(new_calendar_name)
        containing_layout.addLayout(new_horizontal_layout)
        new_delete_calendar_button = QtWidgets.QToolButton(self.scrollAreaWidgetContents)
        new_delete_calendar_button.setText('Delete')
        self._config_delete_button_color(new_delete_calendar_button)
        new_delete_calendar_button.clicked.connect(lambda: self.delete_calendar(new_horizontal_layout))
        new_horizontal_layout.addWidget(new_delete_calendar_button)

    def save(self):
        vlayout = self.verticalLayout
        calendars = []
        for i in range(vlayout.count()):
            layout_item = vlayout.itemAt(i)
            layout = layout_item.layout()
            for j in range(layout.count()):
                widget = layout.itemAt(j).widget()
                if isinstance(widget, QLineEdit):
                    calendars.append(widget.text())
                    break
        self.config['my_calendars'] = calendars
        configs_worker.update_configs_on_disc(self.config)
        print(calendars)

